namespace Aspose {

namespace Pdf {

/// <summary>
/// Enumeration of possible rotation values.
/// </summary>
enum class Rotation
{
    /// <summary>
    /// Non-rotated.
    /// </summary>
    None,
    /// <summary>
    /// Rotated on 90 degrees clockwise.
    /// </summary>
    on90,
    /// <summary>
    /// Rotated on 180 degrees.
    /// </summary>
    on180,
    /// <summary>
    /// Rotated on 270 degrees clockwise.
    /// </summary>
    on270
};

} // namespace Pdf
} // namespace Aspose

#endif // _Aspose_Pdf_Rotation_h_

