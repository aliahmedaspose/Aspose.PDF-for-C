namespace Aspose {

namespace Pdf {

namespace Facades {

/// <summary>
/// Class represents blending color space.
/// </summary>
enum class BlendingColorSpace
{
    /// <summary>
    /// The blending color space is not changed.
    /// </summary>
    DontChange,
    /// <summary>
    /// The blending color space is determined automatically.
    /// </summary>
    Auto,
    /// <summary>
    /// Blending color space is set to DeviceRGB.
    /// </summary>
    DeviceRGB,
    /// <summary>
    /// Blending color space is set to DeviceCMYK.
    /// </summary>
    DeviceCMYK
};

} // namespace Facades
} // namespace Pdf
} // namespace Aspose

#endif // _Aspose_Pdf_Facades_BlendingColorSpace_h_

