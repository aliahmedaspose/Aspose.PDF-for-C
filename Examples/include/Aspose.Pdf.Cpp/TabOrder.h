#ifndef _Aspose_Pdf_TabOrder_h_
#define _Aspose_Pdf_TabOrder_h_

namespace Aspose {

namespace Pdf {

/// <summary>
/// Tab order on the page 
/// </summary>
enum class TabOrder
{
    /// <summary>
    /// No tab order.
    /// </summary>
    None,
    /// <summary>
    /// Row order.
    /// </summary>
    Row,
    /// <summary>
    /// Column order.
    /// </summary>
    Column,
    /// <summary>
    /// Default tab order.
    /// </summary>
    Default,
    /// <summary>
    /// Manual tab order. 
    /// </summary>
    Manual
};

} // namespace Pdf
} // namespace Aspose

#endif // _Aspose_Pdf_TabOrder_h_

