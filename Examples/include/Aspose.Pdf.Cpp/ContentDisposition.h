namespace Aspose {

namespace Pdf {

/// <summary>
/// MIME protocol Content-Disposition header.
/// </summary>
enum class ContentDisposition
{
    /// <summary>
    /// Result is shown inline.
    /// </summary>
    Inline = 0,
    /// <summary>
    /// Result is saved as attachment.
    /// </summary>
    Attachment = 1
};

} // namespace Pdf
} // namespace Aspose

#endif // _Aspose_Pdf_ContentDisposition_h_

