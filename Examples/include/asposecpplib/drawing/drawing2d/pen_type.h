#ifndef _aspose_drawing_drawing2d_pen_type_h_
#define _aspose_drawing_drawing2d_pen_type_h_

namespace System { namespace Drawing { namespace Drawing2D {

enum class PenType
{
    //
    // Summary:
    //     Specifies a solid fill.
    SolidColor = 0,
    //
    // Summary:
    //     Specifies a hatch fill.
    HatchFill = 1,
    //
    // Summary:
    //     Specifies a bitmap texture fill.
    TextureFill = 2,
    //
    // Summary:
    //     Specifies a path gradient fill.
    PathGradient = 3,
    //
    // Summary:
    //     Specifies a linear gradient fill.
    LinearGradient = 4
};

}}}

#endif